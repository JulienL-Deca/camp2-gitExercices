# Initialisation d’un répertoire pour git
# $ git init

# Changer de repository
# $ git checkout name

# Obtenir info d’un repository git, à faire en fin de journée et après chaque commit pour s’assurer que tout est clean
# $ git status

# Voir les changements effectué dans une branche
# $ git log

# Voir le détail des changements sous forme de diff
# $ git log -p // voir toutes les modifs
# $ git show ID_COMMIT // voir uniquement la modif ID_COMMIT
# $ git diff ID_COMMIT
